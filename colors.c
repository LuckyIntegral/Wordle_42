#include "wordle.h"

void	put_gray(char c)
{
	printf("\033[1;30m%c\033[0m", c);
}

void	put_yellow(char c)
{
    printf("\033[1;33m%c\033[0m", c);
}

void	put_green(char c)
{
    printf("\033[1;32m%c\033[0m", c);
}
